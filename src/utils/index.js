//     treeData：树状数据，应该是一个数组，其中每个元素都是一个节点对象，包含 id、label、children 等属性。
// parent：父节点的 ID。如果节点没有父节点，则 parent 应该为 null。
// result：用于存储结果的数组。在初始调用时，应该将其设置为空数组。
export function flattenTreeData(treeData, parent = null, result = []) {
  for (let i = 0; i < treeData.length; i++) {
    const node = treeData[i];

    const { sysAreaList, ...rest } = node;
    const flatNode = {
      ...rest,
      parent,
    };
    result.push(flatNode);
    if (sysAreaList) {
      flattenTreeData(sysAreaList, node.parentAreaCode, result);
    }
  }
  return result;
}

// 字典依照code返回name
// list字典列表，code对比的值，name对比的参数名(字符串)，Rname需要返回的参数名（字符串）
export function dictLookup(list, code, name, Rname) {
  const data = list.find((item) => {
    return item[name] == code;
  });
  if (data) {
    return data[Rname] ? data[Rname] : "";
  } else {
    return "";
  }
}

// 下载类型
export function blobType(type) {
  if (type == "application/msword") {
    return "application/vnd.openxmlformats-officedocument.wordprocessingml.document;charset=UTF-8";
  } else if (type == "application/pdf") {
    return "application/pdf;charset=UTF-8";
  } else if (
    type ==
      "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet" ||
    type == "application/vnd.ms-excel"
  ) {
    return "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet;charset=UTF-8";
  } else if (
    type ==
    "application/vnd.openxmlformats-officedocument.presentationml.presentation"
  ) {
    return "application/vnd.openxmlformats-officedocument.presentationml.presentation;charset=UTF-8";
  } else if (type == "video/mp4") {
    return "video/mp4;charset=UTF-8";
  } else if (type == "audio/mp4") {
    return "audio/mp4;charset=UTF-8";
  } else if (type == "application/x-zip-compressed") {
    return "application/x-zip-compressed;charset=UTF-8";
  }
}

// 下载文件
export function downloadFile(data, fileName) {
  if (!data) return;
  const blob = new Blob([data], {
    type: blobType(data.type),
  });
  const url = window.URL.createObjectURL(blob);
  if ("download" in document.createElement("a")) {
    const a = document.createElement("a");
    a.href = url;
    a.download = fileName;
    a.style.display = "none";
    document.body.appendChild(a);
    a.click();
    URL.revokeObjectURL(a.href);
    document.body.removeChild(a);
  } else {
    navigator.msSaveBlob(blob, fileName);
  }
}

// 16进制转成rgb
export function set16ToRgb(str) {
  const reg = /^#([0-9A-Fa-f]{3}|[0-9A-Fa-f]{6})$/;
  if (!reg.test(str)) {
    return;
  }
  let newStr = str.toLowerCase().replace(/\#/g, "");
  const len = newStr.length;
  if (len == 3) {
    let t = "";
    for (let i = 0; i < len; i++) {
      t += newStr.slice(i, i + 1).concat(newStr.slice(i, i + 1));
    }
    newStr = t;
  }
  const arr = []; // 将字符串分隔，两个两个的分隔
  const num = 6;
  for (let i = 0; i < num; i = i + 2) {
    const s = newStr.slice(i, i + 2);
    arr.push(parseInt("0x" + s));
  }
  return "rgba(" + arr.join(",") + ",0.2)";
}
